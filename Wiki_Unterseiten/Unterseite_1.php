						<h3>Einführung und Ziele des Projektes</h3>
						<p>„Schule wird Staat“ – was heißt das eigentlich? Die Idee hinter dem Projekt ist es, mit Hilfe eines Rollenspiels unsere Schule für eine Woche in einen Staat zu verwandeln. Der Ansatz, des im Sommer 2021 stattfindenden Projekt ist, dass jede*r Schüler*in und jede*r Lehrer*in vor der Woche eine Rollenkarte bekommt, die seine/ihre Funktion im Staat klarstellt. Man bekommt so die Möglichkeit, für eine Woche in z.B. die Welt der Politik, der Kunstschaffenden oder des Polizeiwesens einzutauchen und den Staat und die Gesellschaft hautnah mitzuerleben. Neben der Möglichkeit viele Erfahrungen zu sammeln, unterschiedliche Lebensbereiche bzw. Berufsgruppen besser kennenzulernen und als Bürger*in sein eigenes Geld zu verdienen, soll das Projekt selbstverständlich auch ordentlich Spaß machen. Das Konzept, dass man eine Schule in einen Staat umfunktioniert ist kein Neues, nun soll es allerdings auch an unserer Schule stattfinden und wir können es kaum erwarten.<br><br></br>Das Ziel des Projektes ist es, den Schüler*innen zu ermöglichen, bessere Einblicke in die Funktionsweise eines Staates erhalten, als das normalerweise der Fall wäre. Besonders wichtig war dabei die Entwicklung eines Systems, das demokratische Vorgänge und Strukturen ermöglicht, um diese auch Schüler*innen jüngerer Semester näher zu bringen und ein Interesse für Politik zu wecken und oder zu pflegen. Des Weiteren ist es ein Anliegen des Projekts, der Schüler*innenschaft, die weitestgehend aus wohlhabenden Akademiker*innenhaushalten kommt, auch Wertschätzung für andere Soziale Klassen und Berufsgruppen beizubringen, denn gegenseitige Wertschätzung und Achtung ist Voraussetzung für eine gesunde Gesellschaft, in der ein*e Fabrikarbeiter*in genauso geachtet und geschätzt wird, wie ein*e Abgeordnete*r. Das Simulieren eines Staates ist hierbei lediglich ein Instrument um Frustrationen, Ablehnungen usw. wie manchmal in einem „normalen“ Unterricht, zu umgehen und Spaß mit Lernen mehr zu verbinden, als dies in einer einzelnen Schulklasse der Fall wäre. Der große Maßstab (gesamte Schule) des Projekts ist weiterhin ein wichtiger Faktor. Das Organisationsteam erhofft sich davon einen größeren Zusammenhalt/ ein größeres Gemeinschaftsgefühl der gesamten Schule.</br> </p>
